
public class Nestedelseif 
{

	public static void main(String[] args) 
	{
		
		int a = 100;
		int b = 20;
		int c = 150;
		
		if (a>b) 
		{
			if (a>c)
			{
				System.out.println(" a is greater");
			}
			else 
			{
				System.out.println("b is greatest");
			}
		}
		else 
		{
			System.out.println("c is greatest");
		}
	}
}
		

